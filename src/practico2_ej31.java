public class practico2_ej31 {
    public static void main (String args[]) {
        // Imprimir los números del 10 al 1000, de 10 en 10.

        System.out.println("El programa imprime los números desde 10 hasta 1000, incrementando de 10 en 10:");
        int i=10;

        while (i<=1000) {
            System.out.println(i);
            i=i+10;
        }

        System.out.println("Fin.");
    }
}
