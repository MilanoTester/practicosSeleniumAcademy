public class practico2_ej19 {
    public static void main (String args[]) {
        // Imprimir los números del 20 al 10, de forma decreciente.

        //Descripción del programa
        System.out.println("El programa muestra en pantalla los números desde 20 hasta 10, de forma decreciente.");

        for (int i=20; i>=10; i=i-1) {
            System.out.println(i);
        }

        System.out.println("El programa ha finalizado.");

    }
}
