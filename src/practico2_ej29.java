public class practico2_ej29 {
    public static void main (String args[]) {
        // Programa Java que muestre los números del 1 al 10 utilizando la instrucción while

        System.out.println("El programa muestra los números desde 1 hasta 10:");
        int i=1;

        while (i<=10) {
            System.out.println(i);
            i++;
        }
        System.out.println("Fin.");
    }
}
