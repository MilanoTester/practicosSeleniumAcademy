public class practico2_ej20 {
    public static void main (String args[]) {
        // Programa Java que muestre los números del 1 al 10 utilizando la instrucción for

        //Presentación del programa
        System.out.println("El programa muestra en pantalla los números desde 1 hasta 10.");

        for (int i=1; i<=10; i++) {
            System.out.println(i);
        }

        System.out.println("El programa ha finalizado.");
    }
}
