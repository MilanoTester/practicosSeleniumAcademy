
import java.util.ArrayList;

public class ArrayUtils {

    // Imprimir arreglo Enteros
    public static void imprimirArregloEnteros(Integer[]arregloEnteros) {
        for (Integer num:arregloEnteros) {
            System.out.println(num);
        }
    }

    // Imprimir arreglo Float
    public static void imprimirArregloFloat(Float[]arregloFloat) {
        for (Float num:arregloFloat) {
            System.out.println(num);
        }
    }

    // Imprimir arreglo Double
    public static void imprimirArregloDouble(Double[]arregloDouble) {
        for (Double num:arregloDouble) {
            System.out.println(num);
        }
    }

    // Imprimir arreglo String
    public static void imprimirArregloString(String[]arregloString) {
        for (String num:arregloString) {
            System.out.println(num);
        }
    }

}
